package Day9Package;

public class OddEven {
    public static void main(String [] args){
        System.out.println("Another Logic For Odd Numbers");
        int i;
        for(i=0;i<5;i++){
            int odd1=2*i+1;
            System.out.println(odd1);
        }

        System.out.println("Another Logic For Even Numbers");
        int y;
        for(y=1;y<=5;y++){
            int even =2*y;
            System.out.println(even);
        }
    }
}
