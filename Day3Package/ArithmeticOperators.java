package Day3Package;

public class ArithmeticOperators {
    public static void main(String[] args){
        int a=5;
        int b=2;
        System.out.println("Addition ArithmeticOperators");
        int c=a+b;
        System.out.println(c);
        System.out.println("Subtraction ArithmeticOperators");
        int s=a-b;
        System.out.println(s);
        System.out.println("Multiplication ArithmeticOperators");
        int y=a*b;
        System.out.println(y);
        System.out.println("Division ArithmeticOperators");
        int o=a/b;
        System.out.println(o);
        System.out.println("Modulo ArithmeticOperators");//it  gives remainder
        int f=a%b;
        System.out.println(f);
    }
}
